    private String displayActorName(ResultSet rs) throws SQLException {
        String answer = null;
        while (rs.next()) {
            answer = (rs.getString("name"));
        }
        return answer;
    }

    private String displayActorSurname(ResultSet rs) throws SQLException {
        String answer = null;
        while (rs.next()) {
            answer = (rs.getString("surname"));
        }
        return answer;
    }

    private String displayCreatedDate(ResultSet rs) throws SQLException {
        String answer = null;
        while (rs.next()) {
            answer = (rs.getString("created_at"));
        }
        return answer;
    }

    @Override
    public void databaseChecker() {
        String patientName = String.format("SELECT name FROM patient WHERE fin = '%s'", getFin());
        String patientSurname = String.format("SELECT surname FROM patient WHERE fin = '%s'", getFin());
        String createdAt = String.format("SELECT created_at FROM patient WHERE fin = '%s'", getFin());
        try (Connection conn = SQL.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(patientName)) {
            String answer = displayActorName(rs);
            this.name = answer;
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }

        try (Connection conn = SQL.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(patientSurname)) {
            String answer = displayActorSurname(rs);
            this.surname = answer;
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }

        try (Connection conn = SQL.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(createdAt)) {
            String answer = displayCreatedDate(rs);
            this.createdDate = answer;
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }

    @Override
    public void fillTextField() {
        databaseChecker();
        nameText.setText(name);
        surnameText.setText(surname);
        finText.setText(getFin());
        createdText.setText(createdDate);
    }

